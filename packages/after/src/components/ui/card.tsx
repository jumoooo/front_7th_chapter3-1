const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({ className, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "rounded-sm border shadow-sm",
      // bg-card는 기본값이지만, className에 bg-* 클래스가 있으면 덮어쓰도록 함
      // className이 나중에 오므로 더 높은 우선순위를 가짐
      "bg-card",
      className
    )}
    {...props}
  />
));
Card.displayName = "Card";

